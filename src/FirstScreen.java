    String folder="";
    @Override
    public FileVisitResult preVisitDirectory(Path dir, BasicFileAttributes attrs) throws IOException {
        //System.out.println("About to visit : "+dir.getFileName() +"     "+dir.getParent() + dir.getNameCount());
        if(dir.getNameCount()==4){
            folder=dir.getFileName().toString();
        }
        return FileVisitResult.CONTINUE;
    }

    @Override
    public FileVisitResult visitFile(Path file, BasicFileAttributes attrs) throws IOException {
        //System.out.println("Visited file : "+file);
        String name=file.getFileName().toString();
        if(name.contains(".mp3") || name.contains(".jpg")){
            list.add(name);
        }
        
        return FileVisitResult.CONTINUE;
    }

    @Override
    public FileVisitResult visitFileFailed(Path file, IOException exc) throws IOException {
        //System.out.println("File visit failed : "+file);
        list.remove(file.getFileName().toString());
        return FileVisitResult.CONTINUE;
    }

    @Override
    public FileVisitResult postVisitDirectory(Path dir, IOException exc) throws IOException {
        //System.out.println("Visited dir : "+dir.getFileName());
        int count=dir.getNameCount();
        String key=folder+"-"+dir.getFileName()+" (80s)";
        ArrayList<String> listClone=new ArrayList<>();
        if(count==5){
            listClone.addAll(list);
            songs.put(key, listClone);
            
            list.clear();
            //System.out.println(songs.get(key));
        }
        
        return FileVisitResult.CONTINUE;
    }
    
